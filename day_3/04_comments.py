# step_01: when to use the double quotation?
print("my name is Ejaz") # output: my name is Ejaz and it is a string

# step_02: when to use single quotation?
# When a string contains double quotations, it's more convenient to use single quotations to define the string. This avoids the need to escape the double quotations.
print('this is a "quoted" string') # string containg double quotation

# step_03: when to use triple quotation?
print('''this is used to write multiline strings in the code ''')

